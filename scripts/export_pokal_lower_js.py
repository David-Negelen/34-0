#!/usr/bin/env python3
"""
Patch ratings in src/data/pokalPlayers.js using scraped data.

  • Clubs scraped from fifaindex  → real player OVR ratings (name-matched)
  • Unscraped 3. Liga clubs       → flat rating 60
  • Regionalliga / amateur clubs  → flat rating 50
  • Removes duplicates from pokalPlayers.js

Usage:
    python scripts/export_pokal_lower_js.py
"""

